    public AsignarNota(String carne, String curso) {
        initComponents();

        cursoSeleccionado = curso;
        jTextField1.setEditable(false);
        jTextField2.setEditable(false);
        jTextField3.setEditable(false);

        for (Estudiante estudiante : LoginA.estudiantes) {
            if (estudiante.carne.equals(carne)) {
                estudianteSeleccionado = estudiante;
                break;
            }
        }

        jTextField1.setText(estudianteSeleccionado.nombre);
        jTextField2.setText(estudianteSeleccionado.apellido);
        jTextField3.setText(estudianteSeleccionado.carne);

        if (estudianteSeleccionado.nota.get(cursoSeleccionado) != null) {
            jTextField4.setText(estudianteSeleccionado.nota.get(cursoSeleccionado).toString());
        } else {
            jTextField4.setText("");
        }
    }

