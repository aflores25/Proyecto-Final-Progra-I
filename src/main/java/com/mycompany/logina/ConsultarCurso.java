    public static void leer(String rutaConNombre) {

        JSONParser jp = new JSONParser();

        try {
            FileReader r = new FileReader(rutaConNombre);
            Object obj = jp.parse(r);

            JSONArray cursoList = (JSONArray) obj;

            for (Object l : cursoList) {
                JSONObject curso = (JSONObject) l;
                Cursos c = new Cursos();
                c.id = (String) curso.get("id");
                c.nombre = (String) curso.get("nombre");
                c.seccion = (String) curso.get("seccion");

                SimpleDateFormat dateFormat = new SimpleDateFormat("dd-MM-yyyy");
                Date fechaInicio = null;
                Date fechaFin = null;
                try {
                    fechaInicio = dateFormat.parse((String) curso.get("fecha_inicio"));
                    fechaFin = dateFormat.parse((String) curso.get("fecha_fin"));
                } catch (ParseException ex) {

                }

                c.fechai = fechaInicio;
                c.fechaf = fechaFin;
                c.horai = (String) curso.get("hora_inicio");
                c.horaf = (String) curso.get("hora_fin");
                c.profesor = (String) curso.get("profesor");
                
                LoginA.cursos.add(c);
            }

        } catch (Exception e) {

            e.printStackTrace();
        }
    }
    
    private void actualizarTabla(){
        
        DefaultTableModel data = new DefaultTableModel(new String[]{"ID", "Nombre", "Secci√≥n", "Fecha Inicio", "Fecha Fin", "Hora Inicio", "Hora Fin", "Profesor"}, LoginA.cursos.size());
        jTable1.setModel(data);

        int row = 0;

        for (Cursos c : LoginA.cursos) {
            jTable1.setValueAt(c.id, row, 0);
            jTable1.setValueAt(c.nombre, row, 1);
            jTable1.setValueAt(c.seccion, row, 2);
            jTable1.setValueAt(c.fechai, row, 3);
            jTable1.setValueAt(c.fechaf, row, 4);
            jTable1.setValueAt(c.horai, row, 5);
            jTable1.setValueAt(c.horaf, row, 6);
            jTable1.setValueAt(c.profesor, row, 7);
            row++;
        }
    }

