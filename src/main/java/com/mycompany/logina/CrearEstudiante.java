    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Estudiante e = new Estudiante();
        e.nombre = jTextField1.getText();
        e.apellido = jTextField2.getText();
        e.carne = jTextField3.getText();
        e.password = jPasswordField1.getText();

        if (Password.verificacion(e.password)) {
            LoginA.estudiantes.add(e);
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jPasswordField1.setText("");
        } else {
            JOptionPane.showMessageDialog(this, "Password no cumple los requisitos");
        }


    }//GEN-LAST:event_jButton1ActionPerformed

