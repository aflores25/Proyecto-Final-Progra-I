    private void actualizarTabla() {
        DefaultTableModel data = new DefaultTableModel(new String[]{"ID", "Nombre", "Secci√≥n", "Fecha Inicio", "Fecha Fin", "Hora Inicio", "Hora Fin", "Profesor", "Nota"}, LoginA.cursos.size());
        jTable1.setModel(data);

        int row = 0;

        for (Cursos c : LoginA.estudianteLogeado.cursos) {
            jTable1.setValueAt(c.id, row, 0);
            jTable1.setValueAt(c.nombre, row, 1);
            jTable1.setValueAt(c.seccion, row, 2);
            jTable1.setValueAt(c.fechai, row, 3);
            jTable1.setValueAt(c.fechaf, row, 4);
            jTable1.setValueAt(c.horai, row, 5);
            jTable1.setValueAt(c.horaf, row, 6);
            jTable1.setValueAt(c.profesor, row, 7);
            String nota = "";
            if (LoginA.estudianteLogeado.nota.get(c.nombre) != null) {
                 nota = LoginA.estudianteLogeado.nota.get(c.nombre).toString();
            } 
            jTable1.setValueAt(nota, row, 8);

            row++;
        }

    }

