    public void actualizarTabla() {

        DefaultTableModel data = new DefaultTableModel(new String[]{"Carn√©", "Nombre", "Apellido", "Password"}, LoginA.estudiantes.size());
        jTable1.setModel(data);

        int row = 0;

        for (Estudiante e : LoginA.estudiantes) {
            jTable1.setValueAt(e.carne, row, 0);
            jTable1.setValueAt(e.nombre, row, 1);
            jTable1.setValueAt(e.apellido, row, 2);
            jTable1.setValueAt(e.password, row, 3);

            row++;
        }

    }

    public void leerXml(File archivo){
        try{
            DocumentBuilderFactory dbf= DocumentBuilderFactory.newInstance();
            DocumentBuilder db = dbf.newDocumentBuilder();
            Document doc = db.parse(archivo);
            
            doc.getDocumentElement().normalize();
            NodeList nodos = doc.getElementsByTagName("estudiante");
            
            for(int i=0; i<nodos.getLength();i++){
                Node nodo = nodos.item(i);
                if(nodo.getNodeType() == Node.ELEMENT_NODE){
                    Element e =(Element) nodo;
                    Estudiante es = new Estudiante ();
                    es.carne = e.getElementsByTagName("carne").item(0)
                            .getTextContent();
                    es.nombre = e.getElementsByTagName("nombre").item(0)
                            .getTextContent();
                    es.apellido = e.getElementsByTagName("apellido").item(0)
                            .getTextContent();
                    es.password = e.getElementsByTagName("password").item(0)
                            .getTextContent();
                    LoginA.estudiantes.add(es);
                }
            }
            actualizarTabla();
        }catch(Exception ex){
        }
        
    }
    
    public void generarArchivo (String ruta, String datos){
        try{
            FileWriter fw = new FileWriter(ruta);
            PrintWriter pw = new PrintWriter(fw);
            pw.write(datos);
            pw.close();
            fw.close();
        }catch (IOException e){
            e.printStackTrace();
        }
    }
    
